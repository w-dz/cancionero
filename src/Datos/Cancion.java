
package Datos;

/**
 *
 * @author Wilson
 */
public class Cancion {
	int cod_canciones;
	String nom_canciones;
	String album_canciones;
	String artista_canciones;
	String letra_canciones;

	public Cancion() {
		
	}

	public int getCod_canciones() {
		return cod_canciones;
	}

	public void setCod_canciones(int cod_canciones) {
		this.cod_canciones = cod_canciones;
	}

	public String getNom_canciones() {
		return nom_canciones;
	}

	public void setNom_canciones(String nom_canciones) {
		this.nom_canciones = nom_canciones;
	}

	public String getAlbum_canciones() {
		return album_canciones;
	}

	public void setAlbum_canciones(String album_canciones) {
		this.album_canciones = album_canciones;
	}

	public String getArtista_canciones() {
		return artista_canciones;
	}

	public void setArtista_canciones(String artista_canciones) {
		this.artista_canciones = artista_canciones;
	}

	public String getLetra_canciones() {
		return letra_canciones;
	}

	public void setLetra_canciones(String letra_canciones) {
		this.letra_canciones = letra_canciones;
	}
}


