package Datos;

/**
 *
 * @author Wilson
 */
public class Artista {
	
	String cod_artista;
	String nom_artista;
	String gen_artista;

	public Artista() {
	
	}

	public String getCod_artista() {
		return cod_artista;
	}

	public void setCod_artista(String cod_artista) {
		this.cod_artista = cod_artista;
	}

	public String getNom_artista() {
		return nom_artista;
	}

	public void setNom_artista(String nom_artista) {
		this.nom_artista = nom_artista;
	}

	public String getGen_artista() {
		return gen_artista;
	}

	public void setGen_artista(String gen_artista) {
		this.gen_artista = gen_artista;
	}
	
	
}
