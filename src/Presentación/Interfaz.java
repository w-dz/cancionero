	public void validarDatosIngresados(){
		String artista = tf_nomArtista.getText().trim();
		String album = tf_nomAlbum.getText().trim();
		String cancion = tf_nomCancion.getText().trim();
		String letra = tArea_letra.getText().trim();
		String genero = (String) cbx_genero.getSelectedItem();
		String codArtista = tf_codArtista.getText();
		String codAlbum = tf_codAlbum.getText();
		
		//Habilida el botón 'Guardar'
		if(longitudBytes == 0 || artista.isEmpty() || album.isEmpty() || cancion.isEmpty() || letra.isEmpty() || genero.isEmpty() || codArtista.isEmpty() || codAlbum.isEmpty()){
			btn_guardar.setEnabled(false);
		}else{
			btn_guardar.setEnabled(true);
		}
	}
	
