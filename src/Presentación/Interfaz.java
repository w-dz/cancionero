	private void cargarImagen(){
		JFileChooser j =  new JFileChooser();
		FileNameExtensionFilter filtro = new FileNameExtensionFilter("JPG & PNG", "jpg", "png");
		j.setFileFilter(filtro);		
		int estado = j.showOpenDialog(null);
		if(estado == JFileChooser.APPROVE_OPTION){			
			try {
				fis = new FileInputStream(j.getSelectedFile());
				this.longitudBytes = (int) j.getSelectedFile().length();
				lbl_imagen.setEnabled(true);
				try {
					lbl_imagen.setIcon(null);				
					Image icono = ImageIO.read(j.getSelectedFile()).getScaledInstance(lbl_imagen.getWidth(), lbl_imagen.getHeight(), Image.SCALE_DEFAULT);
					lbl_imagen.setIcon(new ImageIcon(icono));
					lbl_imagen.updateUI();
					} catch (IOException ex) {
						JOptionPane.showMessageDialog(null, "Error, no se pudo cargar la imagen " + ex);
					}				
			} catch (FileNotFoundException e) {
				JOptionPane.showMessageDialog(null, "Error, archivo no encontrado " + e);
			}
		}
	}
	
		public void consultarCancion() throws SQLException {
		String sql = "select letra_canciones, album.cod_album, album.img_album, artistas.cod_artista, nom_artista from canciones inner join album on album.cod_album = canciones.cod_album "
				+ "inner join artistas on artistas.cod_artista = canciones.cod_artista where nom_canciones like '%" + tf_cancion.getText() + "%'";

		conn = new Conexion();
		Statement st = conn.conn.createStatement();
		ResultSet rs = st.executeQuery(sql);
		InputStream is;		
		//Boolean existeRegistro = false;
		String valor = tf_cancion.getText();
			try {
				if(valor.isEmpty()){
					JOptionPane.showMessageDialog(null, "Ingrese el nombre de alguna canción para buscar");
				}else {
					if(rs.next()){
								
							tf_artista.setText(rs.getString("nom_artista"));
							tf_codArt.setText(rs.getString("cod_artista"));
							tf_codAlb.setText(rs.getString("cod_album"));
							txtareaLetra.setText(rs.getString("letra_canciones"));
							//Trayendo la imagen
							is = rs.getBinaryStream("img_album");
							BufferedImage bi = ImageIO.read(is);
							ImageIcon foto = new ImageIcon(bi);
							Image img = foto.getImage();
							Image newimg = img.getScaledInstance(lbl_cover.getWidth(), lbl_cover.getHeight(), Image.SCALE_SMOOTH);
							ImageIcon newicon = new ImageIcon(newimg);
							lbl_cover.setIcon(newicon);
							lbl_cover.updateUI();

					}else{
						JOptionPane.showMessageDialog(null, "Error, no hay registros");
					}
				}
			} catch (HeadlessException | IOException | SQLException e) {
				JOptionPane.showMessageDialog(null, "Error " + e);
			}finally{
				conn.desconectar();
				System.out.println("Desconectado después de consultar la canción");
			}
	}
	
	public void borrarCancion(){
		conn = new Conexion();
		String nomCancion = tf_cancion.getText();
		String query = "delete canciones, album, artistas from canciones inner join album on album.cod_album "
				+ "= canciones.cod_album inner join artistas on artistas.cod_artista = canciones.cod_artista "
				+ "where nom_canciones like '%" + nomCancion + "%'";
		try {
			PreparedStatement st = conn.conn.prepareStatement(query);
			st.executeUpdate();
			String cadena = "Se eliminó la canción: " + nomCancion.toUpperCase();
			JOptionPane.showMessageDialog(null, cadena);
		} catch (SQLException e) {
			JOptionPane.showMessageDialog(null, "No se pudo borrar el registro " + e);
		}finally{
			conn.desconectar();
			System.out.println("Desconectado después de borrar canción");
		}
	}


